# Вводные данные
A = int(input())
B = input()
C = int(input())

# Возможные базовые операции
if B == '-':
    print(A - C)
elif B == '+':
    print(A + C)
elif B == '%':
    print(A % B)
elif B == '//':
    print(A // B)
elif B == '*':
    print(A * B)
elif B == '**':
    print(A ** B)
elif B == '/':
    print(A / B)
